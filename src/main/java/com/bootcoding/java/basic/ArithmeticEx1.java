package com.bootcoding.java.basic;

public class ArithmeticEx1 {
    public static void main(String[] data) {

        int a = 10;
        int b = 15;
        int c = a + b;
        int d = a - b;
        int e = a - b;
        int f = a * b;
        double g = a / b;
        int h = a % b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.print(h);

    }

}